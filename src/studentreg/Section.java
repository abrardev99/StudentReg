package studentreg;


public class Section {
    private int id;
    private int capacity;

    public Section(int id, int capacity) {
        this.id = id;
        this.capacity = capacity;
    }

    public int getId() {
        return id;
    }

    public int getCapacity() {
        return capacity;
    }
    
    
    
}
